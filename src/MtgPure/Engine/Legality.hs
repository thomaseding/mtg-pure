module MtgPure.Engine.Legality (
  Legality (..),
  toLegality,
  fromLegality,
) where

import safe Data.Typeable (Typeable)

data Legality
  = Legal
  | Illegal
  deriving (Eq, Ord, Show, Typeable)

toLegality :: Bool -> Legality
toLegality = \case
  True -> Legal
  False -> Illegal

fromLegality :: Legality -> Bool
fromLegality = \case
  Legal -> True
  Illegal -> False
